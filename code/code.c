volatile int wave_mode = -1;

float domain_data = 1;
float freq_data = 1;
int analog = 0;


void Initialize_Counters() {

  lcd.begin(16, 2);
  DDRD &= (0xFB);
  //DDRC |= (1<<0);
  //Timer0--------------//
  DDRB |= (1 << 3); //*Set PD6(OC0A) pin as output
  TCCR2A = 0x83;  //*Fast PWM Mode(TOP = 0xFF, update OCR0A register at BOTTOM)
  //*Non-inverting PWM on pin OC0A (clear OC0A pin on compare match, set 0C0A pin at BOTTOM)
  //*OC0B disconnected
  TCCR2B = 0x02;  //*No prescaling
  OCR2A = pgm_read_byte(&wave_data[wave_mode][0]);

  //Timer1-------------//
  cli();
  TCCR1A = 0x00;  //*CTC Mode
  //*TOP = OCR1A, Update 0CR1A Immediate
  TCCR1B = 0x09;  //*no presacalar
  

  OCR1A = SAMPLE_RATE;

  TIMSK1 |= (1 << OCIE1A);
  EIMSK |= (1 << INT0);
  sample = 0;
  sei();
}
void domain() {
  domain_data = (float)analogRead(A0) / 1023 ;
  lcd.setCursor(7, 1);
  lcd.print("D:");
  lcd.setCursor(9, 1);
  lcd.print(domain_data);
}

void freq() {
  freq_data = (float)analogRead(A1) / 1023 ;
  lcd.setCursor(0, 1);
  lcd.print("F:");
  lcd.setCursor(2, 1);
  lcd.print(freq_data);
}

void setup()
{
  
  Initialize_Counters();
}

void loop()
{
   if(analog>200){
       domain();
       freq();
       analog = 0;
   }
    
}

ISR(TIMER1_COMPA_vect) {
  if (sample >= wave_data_length) {
    sample = -1;
  }
  else {
    OCR2A = (char)(pgm_read_byte(&wave_data[wave_mode][sample]) * domain_data);
  }
  ++sample; 
  ++analog;

 OCR1A = (int)(freq_data * SAMPLE_RATE);
}

ISR(INT0_vect) {
  wave_mode++;
  if (wave_mode > 7) {
    wave_mode = 0;
  }
  lcd.setCursor(0, 0);
  lcd.print(wave_name[wave_mode]);
  delay(100);
}